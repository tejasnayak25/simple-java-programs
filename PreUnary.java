public class PreUnary {

    public static void main(String[] args) {
        int i=8;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        int j=6;
        System.out.println(j);
        System.out.println(--j);
        System.out.println(j);
    }
}