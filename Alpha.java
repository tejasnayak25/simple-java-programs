public class Alpha {
    int n;
    double d;
    boolean b;
    String name;
    char ch;

    /*
     * Default constructor
     * n = 0;
     * d = 0.0;
     * b = false;
     * name = null
     * ch = ;
     */

    public static void main(String[] args) {
        Alpha a = new Alpha();
        System.out.println(a.n + " " + a.d + " " + a.b + " " + a.name + " " + a.ch);
    }
}
