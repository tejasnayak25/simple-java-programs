public class Array2 {
    public static void main(String[] args) {
        int[] n = {1,2,3,4,5};

        System.out.println(n.length);

        double[] heights = {5.3, 4.2, 5.9};

        System.out.println(heights.length);

        char[] ch = {'e', 'g', 'h', 'g'};
        System.out.println(ch.length);
    }
}
