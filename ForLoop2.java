public class ForLoop2 {
    public static void main(String[] args) {
        for(int i=1;i<6;i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        int n=5;
        char ch='a';
        for(int j=0;j<n;j++) {
            System.out.print(ch+" ");
            ch++;
        }
    }
}